var x=0;
$("#ostan ").click(function () {
    if(x==0){ $(this).children("img").attr("src","../images/icon/mosalas1.png");
        $("#togle-ostan").toggle(800);
        x=1;}
    else {$(this).children("img").attr("src","../images/icon/mosalas2.png");
        $("#togle-ostan").toggle(800);
        x=0;}
});

